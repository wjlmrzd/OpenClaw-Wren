### 自动更新笔记

关联脚本会自动更新原子笔记的 `## 关联原子笔记` 部分：

```markdown
## 关联原子笔记

### 纵向关联
- [[GB50001_5.2.2]] - 父级条款，关联度 0.9
- [[GB50001_5.2.4]] - 相邻条款，关联度 0.8

### 横向关联
- [[GBT50329_4.2.1]] - 同一专业，关联度 0.7
- [[16G101_5.2]] - 图集配套，关联度 0.8
```

## 组合笔记生成

当关联度高的笔记达到一定数量时，提示生成组合笔记：

```
触发条件:
- 高关联笔记数 ≥ 5
- 属于同一专业
- 内容可形成完整体系

示例:
"检测到以下笔记可组合为《混凝土结构设计核心条款》:
- GB50001_5.2.1~5.2.5
- GB50001_6.1~6.3
- 16G101_5.1~5.4

是否生成组合笔记？"
```

