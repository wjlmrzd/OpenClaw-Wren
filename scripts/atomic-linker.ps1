# 原子笔记自动关联器 - 使用指南

## 功能概述

自动分析原子笔记，建立跨来源、跨专业的关联关系。

## 关联类型

### 1. 纵向关联 (同文档)

```
条款层级关系:
第5章 → 5.1 → 5.1.1 → 5.1.1.1

关联规则:
- 5.2.3 ↔ 5.2.2 (相邻条款)
- 5.2 ↔ 5.2.3 (父子条款)
- 5 ↔ 5.2 (章→节)
```

### 2. 横向关联 (跨文档)

```
关联维度:
├─ 同一专业: 结构规范 ↔ 结构图集
├─ 引用关系: 主规范 ↔ 配套规程
└─ 版本关系: 旧版废止 ↔ 新版替代
```

### 3. 术语关联

```
术语网络:
    混凝土强度
    ├─ 立方体抗压强度 ←──→ 棱柱体抗压强度
    ├─ 标准值 ←──→ 设计值
    └─ C30 ←──→ 强度等级
```

## 使用方式

### 自动运行 (每日 02:00)

```
Cron 任务: 知识整理员
触发: 每天 02:00
动作: 
1. 扫描原子笔记目录
2. 分析新增/修改笔记
3. 计算关联度
4. 更新关联关系
```

### 手动触发

```powershell
# 分析单个笔记
.\atomic-linker.ps1 -NotePath "E:\software\Obsidian\vault\knowledge\工程知识\原子笔记\GB50001_5.2.3_混凝土.md"

# 批量分析目录
.\atomic-linker.ps1 -Batch -InputDir "E:\software\Obsidian\vault\knowledge\工程知识\原子笔记"

# 仅生成关联报告
.\atomic-linker.ps1 -ReportOnly -VaultPath "E:\software\Obsidian\vault"
```

## 关联度计算

### 权重因子

| 因子 | 权重 | 说明 |
|------|------|------|
| 同文档 | 0.4 | 来自同一文档 |
| 相邻条款 | 0.3 | 条款编号相近 |
| 同一专业 | 0.2 | 分类相同 |
| 术语重叠 | 0.1 | 关键词重复 |

### 关联阈值

```
高关联 (≥0.7): 自动建立双向链接
中关联 (0.4-0.7): 建立单向链接，建议关联
低关联 (<0.4): 仅记录，暂不链接
```

