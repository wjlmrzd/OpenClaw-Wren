## 格式规范要求 ⚠️

> **重要**：保存笔记时必须严格遵守以下格式要求

### 表格格式要求

| 要求 | 说明 |
|------|------|
| **必须保留** | 原规范表格的列结构、列名、计量单位、单元格内容 |
| **禁止修改** | 合并/拆分单元格、修改列名、改变计量单位 |
| **允许添加** | 行号列（可选）、备注列中文说明 |
| **格式** | 使用 Markdown 标准表格格式 |

**示例**：

```markdown
# 混凝土强度等级表

| 强度等级 | 立方体抗压强度标准值 f_cu,k (MPa) | 备注 |
|---------|-------------------------------|------|
| C15     | 15                            | —    |
| C20     | 20                            | —    |
| C25     | 25                            | —    |
```

### 图片处理规则

| 要求 | 说明 |
|------|------|
| **保持原样** | 不进行格式转换、不压缩、不重命名 |
| **引用格式** | `![](path/to/image.png)` |
| **禁止** | 将图片内容转换为文字描述 |

