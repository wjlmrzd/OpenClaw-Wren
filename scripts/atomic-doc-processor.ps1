# 原子文档解析器 - 使用指南

## 功能概述

解析 PDF、图片等文档，提取原子内容并保存为结构化笔记。

## 使用方式

### 方式1: 通过 OpenClaw 主会话

```
用户: "解析这个 PDF 并保存到工程知识篇章"
     [附件: document.pdf]
```

我会自动：
1. 提取文档内容（使用 summarize 技能）
2. 识别文档类型和规范名称
3. 分割为原子条款
4. 生成带来源标注的笔记
5. 建立关联关系

### 方式2: 通过命令行

```powershell
# 解析 PDF
.\atomic-doc-processor.ps1 -File "C:\path\to\doc.pdf" -VaultPath "E:\software\Obsidian\vault"

# 解析图片
.\atomic-doc-processor.ps1 -File "C:\path\to\image.png" -VaultPath "E:\software\Obsidian\vault" -Type Image

# 批量处理
.\atomic-doc-processor.ps1 -Batch -InputDir "C:\Docs" -VaultPath "E:\software\Obsidian\vault"
```

## 参数说明

| 参数 | 类型 | 说明 |
|------|------|------|
| File | string | 单个文件路径 |
| Batch | switch | 批量模式 |
| InputDir | string | 批量输入目录 |
| VaultPath | string | Obsidian vault 路径 |
| Type | string | 文件类型: PDF/Image/Text |

## 原子笔记命名规则

```
{文档编号}_{条款编号}_{主题词}.md

示例:
GB50001_5.2.3_混凝土强度等级.md
JTG_B01_3.1.2_设计速度标准.md
```

## 元数据提取规则

### 文档编号识别

| 格式 | 示例 | 说明 |
|------|------|------|
| GB/T XXXXX-YYYY | GB/T 50001-2017 | 国家标准 |
| JTG XXXX | JTG D60-2015 | 交通行业标准 |
| JG XXXX | JGJ 130-2011 | 建筑行业标准 |
| QB XXXX | QB/T 2577-2012 | 轻工行业标准 |
| DB XX/XX | DB11/1027-2013 | 地方标准 |

### 版本提取

- 正则: `v?(\d+\.)?\d+[a-z]?|Rev\.?\s*\d+|第\s*\d+\s*版`
- 示例: v2.1, Rev.03, 第3版

### 条款编号提取

- 正则: `\d+(\.\d+)+` 或 `[一二三四五六七八九十]+、`
- 示例: 5.2.3, 3.1.2, 第八章

## 关联分析规则

### 自动关联条件

| 条件 | 关联类型 | 说明 |
|------|---------|------|
| 同一文档相邻条款 | 纵向关联 | 上/下级条款 |
| 不同文档同一专业 | 横向关联 | 专业交叉 |
| 同一术语引用 | 引用关联 | 被引用关系 |

### 关联权重

```
高 (权重≥0.8):  同一文档 + 相邻条款
中 (权重0.5-0.8): 同一专业 + 相关术语
低 (权重<0.5):  跨专业 + 模糊关联
```

